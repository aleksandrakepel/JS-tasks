let num = prompt("What time of year?");
switch (num) {
    case '1':
        alert ('Winter');
        break;

    case '2':
        alert ('Spring');
        break;

    case '3':
        alert ('Summer');
        break;

    case '4':
        alert ('Autumn');
        break;

    default:
        alert('Unknown');
}